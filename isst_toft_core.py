    weights = [0] * n
    for i in range(n):
        for j in range(n):
            if i != j:
                weights[i] += cosine_sim(embeddings[i], embeddings[j])
    total = sum(weights)
    norm_weights = [w / total for w in weights]
    combined = [f"[{norm_weights[i]*100:.1f}%] {outputs[i]}" for i in range(n)]
    return " | ".join(combined)

